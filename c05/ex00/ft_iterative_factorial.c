int	ft_iterative_factorial(int nb)
{
	int	i;
	int	r;

	i = 2;
	r = 1;
	if (nb == 1 || nb == 0)
		return (1);
	if (nb < 0)
		return (0);
	while (i <= nb)
	{
		r = r * i;
		i++;
	}
	return (r);
}
