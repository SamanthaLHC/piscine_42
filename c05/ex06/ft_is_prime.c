int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb == 2)
		return (1);
	while ((i < nb / 2) && (nb % i != 0))
	{
		i++;
	}
	if ((nb % i == 0) || (nb == 0) || (nb == 1))
		return (0);
	else
		return (1);
}
